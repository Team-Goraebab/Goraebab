// 첫 글자 제한: 첫 글자는 반드시 알파벳 대소문자(a-zA-Z) 또는 숫자(0-9)로 시작해야 합니다.
// 사용 가능 문자: 첫 글자 이후에는 알파벳 대소문자(a-zA-Z), 숫자(0-9), 밑줄(_), 마침표(.), 하이픈(-)을 사용할 수 있습니다.
// 길이 제한 없음: 이름의 길이에 대한 제한이 없습니다.
export const containerNamePattern = /^[a-zA-Z0-9][a-zA-Z0-9_.-]*$/;

// 첫 글자 제한 없음: 첫 글자가 알파벳이나 숫자가 아니어도 됩니다.
// 사용 가능 문자: 소문자(a-z), 숫자(0-9), 밑줄(_), 마침표(.), 하이픈(-)만 허용됩니다. 대문자는 허용되지 않습니다.
// 길이 제한: 1자 이상 20자 이하로 길이가 제한됩니다.
export const hostNamePattern = /^[a-z0-9_.-]{1,20}$/;
